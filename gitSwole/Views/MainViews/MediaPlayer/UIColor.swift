import Foundation
import Hex

extension UIColor {
    
    static var spotifyGreen: UIColor {
        return UIColor(hex: "#03bb4f")
    }
    
    static var spotifyBackground: UIColor {
        return UIColor(hex: "#1a1a1a")
    }
    
}
